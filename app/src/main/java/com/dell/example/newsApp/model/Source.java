package com.dell.example.newsApp.model;

/**
 * Class contain the information regarding Source of News
 */

public class Source {
    private String id;
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
